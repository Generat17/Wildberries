package main

import (
	"fmt"
)

// Reverse - —Ñ—É–Ω–∫—Ü–∏—è, –∫–æ—Ç–æ—Ä–∞—è –ø–µ—Ä–µ–≤–æ—Ä–∞—á–∏–≤–∞–µ—Ç —Å—Ç—Ä–æ–∫—É
func Reverse(s string) string {
	// –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ —Å—Ä–µ–∑ —Ä—É–Ω
	runes := []rune(s)

	// –û–±–º–µ–Ω—è–µ–º –º–µ—Å—Ç–∞–º–∏ —ç–ª–µ–º–µ–Ω—Ç—ã —Å—Ä–µ–∑–∞ —Å –Ω–∞—á–∞–ª–∞ –∏ —Å –∫–æ–Ω—Ü–∞
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}

	// –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ä–µ–∑ —Ä—É–Ω –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É
	return string(runes)
}

func main() {
	// –¢–µ—Å—Ç–∏—Ä—É–µ–º —Ñ—É–Ω–∫—Ü–∏—é –Ω–∞ —Ä–∞–∑–Ω—ã—Ö —Å—Ç—Ä–æ–∫–∞—Ö
	fmt.Println(Reverse("–≥–ª–∞–≤—Ä—ã–±–∞")) // –∞–±—ã—Ä–≤–∞–ª–≥
	fmt.Println(Reverse("Hello"))    // olleH
	fmt.Println(Reverse("–ü—Ä–∏–≤–µ—Ç"))   // —Ç–µ–≤–∏—Ä–ü
	fmt.Println(Reverse("üëãüåé"))       // üåéüëã
}
